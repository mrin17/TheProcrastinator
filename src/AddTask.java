    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (jDateChooser2.getDate() == null) {
            JOptionPane.showMessageDialog(this,"You need a start date. Please choose a start date and try again.");
            return;
        }
        if (jDateChooser1.getDate() == null) {
            JOptionPane.showMessageDialog(this,"You need an end date. Please choose an end date and try again.");
            return;
        }
        String name = jTextField1.getText();
        float duration = (jComboBox2.getSelectedIndex()+1)/2f;
        int timeh = jComboBox3.getSelectedIndex()+1;
        int timem = jComboBox4.getSelectedIndex();
        String ampm = jComboBox5.getSelectedIndex()==0 ? "am" : "pm";
        Date startdate = jDateChooser2.getDate();
        Date duedate= jDateChooser1.getDate();
        startdate.setHours(ampm.equals("am") ? timeh : timeh+12);
        startdate.setMinutes(timem);
        startdate.setSeconds(0);
        int duetimeh = jComboBox8.getSelectedIndex()+1;
        int duetimem = jComboBox6.getSelectedIndex();
        String dueampm = jComboBox7.getSelectedIndex()==0 ? "am" : "pm";
        duedate.setHours(dueampm.equals("am") ? duetimeh : duetimeh+12);
        duedate.setMinutes(duetimem);
        duedate.setSeconds(0);
        
        Date enddate = jDateChooser2.getDate();
        enddate.setHours((ampm.equals("am") ? timeh : timeh+12)+(int)duration);
        enddate.setMinutes(timem + (int) ((duration * 60) % 60));
        enddate.setSeconds(0);
        
        Date today = new Date();
        
        if (enddate.getDate() > startdate.getDate()) {
            JOptionPane.showMessageDialog(this,"The duration puts this task into the next day. Please change the duration and try again.");
        }
        else if (duedate.before(startdate)) {
            JOptionPane.showMessageDialog(this,"The due date must be after the start date. Please change either date and try again.");
        }
        else if (duedate.before(enddate)) {
            JOptionPane.showMessageDialog(this,"The duration puts this task past its due date. Please change the duration and try again.");
        }
        else if (startdate.before(today)) {
            JOptionPane.showMessageDialog(this,"This task is scheduled for a time that already passed. Please change the time or date and try again.");
        }
        else if (whichedit >= 0) {
            //this means that this task already exists, so edit it
            Task nTask = new Task(name, duration, startdate, enddate, duedate);
            ((MainScreen) mainscreen).taskList.remove(whichedit);
            ((MainScreen) mainscreen).addTask(nTask);
            ((MainScreen) mainscreen).reformat();
            this.dispose();
        }
        else {
            //create an instance of a task
            Task nTask = new Task(name, duration, startdate, enddate, duedate);
            ((MainScreen) mainscreen).addTask(nTask);
            System.out.println(nTask.debugString());
            this.dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

