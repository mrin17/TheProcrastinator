    public AddTask(java.awt.Frame parent, boolean modal, int whichedit) {
        super(parent, modal);
        initComponents();
        mainscreen = parent;
        this.whichedit = whichedit;
        jLabel1.setText("Edit Task");
        Task t = ((MainScreen) mainscreen).taskList.get(whichedit);
        jTextField1.setText(t.name);
        jDateChooser2.setDate(t.startdate);
        int starthour = t.startdate.getHours();
        if (starthour == 0) {
            jComboBox3.setSelectedIndex(11);
            jComboBox5.setSelectedIndex(0);
        }
        else if (starthour == 12) {
            jComboBox3.setSelectedIndex(11);
            jComboBox5.setSelectedIndex(1);           
        }
        else {
            jComboBox3.setSelectedIndex((starthour % 12)-1);
            jComboBox5.setSelectedIndex((int) starthour / 12);
        }
        jComboBox4.setSelectedIndex(t.startdate.getMinutes());
        int duehour = t.duedate.getHours();
        if (duehour == 0) {
            jComboBox8.setSelectedIndex(11);
            jComboBox7.setSelectedIndex(0);
        }
        else if (duehour == 12) {
            jComboBox8.setSelectedIndex(11);
            jComboBox7.setSelectedIndex(1);           
        }
        else {
            jComboBox8.setSelectedIndex((duehour % 12)-1);
            jComboBox7.setSelectedIndex((int) duehour / 12);
        }
        jComboBox6.setSelectedIndex(t.duedate.getMinutes());
        jDateChooser1.setDate(t.duedate);
        jComboBox2.setSelectedIndex((int) (t.duration*2)-1);
        this.setDefaultCloseOperation(WindowConstants.DO_NOTHING_ON_CLOSE);
    }

